/* Scroll the background layers */
function parallaxScroll(){
	var scrolled = $(window).scrollTop();
	
	//About Scrolling
$('#outer-ring').css('top',(620-(scrolled*.23))+'px');
	$('#middle-ring').css('top',(620-(scrolled*.23))+'px');
	$('#my-face').css('top',(290-(scrolled*.0))+'px');
	$('#inner-ring').css('top',(620-(scrolled*.23))+'px');
	$('#vertical-scale').css('top',(740-(scrolled*.40))+'px');
	$('#target').css('top',(950-(scrolled*.52))+'px');
	$('#about-box').css('top',(950-(scrolled*.52))+'px');
	//Interface Scrolling
	$('#interface-box').css('top',(1350-(scrolled*.52))+'px');
	
	$('#animation-box').css('top',(1900-(scrolled*.52))+'px');
	$('#branch').css('top',(50-(scrolled*.05))+'px');
	
	$('#front-ink1').css('top',(1350-(scrolled*.15))+'px');
	$('#front-ink2').css('top',(200-(scrolled*.0))+'px');
	$('#print-box').css('top',(2200-(scrolled*.42))+'px');
	
}

/* Set navigation dots to an active state as the user scrolls */
function redrawDotNav(){
	var section1Top =  0;
	// The top of each section is offset by half the distance to the previous section.
	var section2Top =  $('#frameless-parachute').offset().top - (($('#english-channel').offset().top - $('#frameless-parachute').offset().top) / 2);
	var section3Top =  $('#english-channel').offset().top - (($('#about').offset().top - $('#english-channel').offset().top) / 2);
	var section4Top =  $('#about').offset().top - (($(document).height() - $('#about').offset().top) / 2);;
	
	
	$('li.menu').removeClass('active');
	if($(document).scrollTop() >= section1Top && $(document).scrollTop() < section2Top){
		$('nav#primary a.manned-flight').addClass('active');
	} else if ($(document).scrollTop() >= section2Top && $(document).scrollTop() < section3Top){
		$('nav#primary a.frameless-parachute').addClass('active');
	} else if ($(document).scrollTop() >= section3Top && $(document).scrollTop() < section4Top){
		$('nav#primary a.english-channel').addClass('active');
	} else if ($(document).scrollTop() >= section4Top){
		$('nav#primary a.about').addClass('active');
	}
	
}


		
		
